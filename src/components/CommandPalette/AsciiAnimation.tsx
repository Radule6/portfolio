"use client";


import { useState, useEffect, useMemo, useRef } from "react";

interface AsciiAnimationProps {
  frames: string[];
  /** ms between frames */
  interval?: number;
  /** Loop forever or play once */
  loop?: boolean;
  /** Called when a non-looping animation finishes */
  onComplete?: () => void;
}

const AsciiAnimation: React.FC<AsciiAnimationProps> = ({
  frames,
  interval = 180,
  loop = false,
  onComplete,
}) => {
  const [frameIndex, setFrameIndex] = useState(0);
  const [done, setDone] = useState(false);
  const timerRef = useRef<ReturnType<typeof setInterval> | null>(null);

  useEffect(() => {
    if (frames.length <= 1 || done) return;

    timerRef.current = setInterval(() => {
      setFrameIndex((prev) => {
        const next = prev + 1;
        if (next >= frames.length) {
          if (loop) return 0;
          // Stop on last frame
          if (timerRef.current) clearInterval(timerRef.current);
          setDone(true);
          onComplete?.();
          return prev;
        }
        return next;
      });
    }, interval);

    return () => {
      if (timerRef.current) clearInterval(timerRef.current);
    };
  }, [frames, interval, loop, done, onComplete]);

  return (
    <pre
      className="text-accent-lime leading-tight select-none"
      style={{
        fontFamily:
          'ui-monospace, "Cascadia Code", "Source Code Pro", Menlo, monospace',
        fontSize: "11px",
        lineHeight: "1.2",
      }}
      aria-hidden="true"
    >
      {frames[frameIndex]}
    </pre>
  );
};

export default AsciiAnimation;

/* в”Җв”Җ Logo reveal вҖ” Matrix-style scramble decode в”Җв”Җ */

const LOGO_COLS = 52;
const LOGO_ROWS = 17;
const SCRAMBLE_POOL = "!@#$%^&*=+~<>{}[]|/\\:;01234567890ABCDEF";

